package com.clubecerto.apps.app.classes;

public class FeaturedStore {

   int id,category_id;
   String name,image;

    public FeaturedStore(int id, int category_id, String name, String image) {
        this.id = id;
        this.category_id = category_id;
        this.name = name;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCategory_id() {
        return category_id;
    }

    public void setCategory_id(int category_id) {
        this.category_id = category_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
